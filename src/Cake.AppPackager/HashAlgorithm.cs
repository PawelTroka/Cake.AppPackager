namespace Cake.AppPackager {
    /// <summary>
    /// Defins the hashing algorithm to use when creating the block map.
    /// </summary>
    public enum HashAlgorithm {
        /// <summary>
        /// Uses SHA256 Algorithm
        /// </summary>
        SHA256,

        /// <summary>
        /// Uses SHA384 Algorithm
        /// </summary>
        SHA384,

        /// <summary>
        /// Uses SHA512 Algorithm
        /// </summary>
        SHA512
    }
}
